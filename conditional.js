function sum(num1, num2){
    return num1 + num2
}
function sub(num1, num2){
    return num1 - num2
}
function divide(num1, num2){
    return num1 / num2
}
function multi(num1, num2){
    return num1 * num2
}
function mod(num1, num2){
    return num1 % num2
}
console.log(sum(30,40));
console.log(sub(300,40));
console.log(mod(30,40));
console.log(multi(30,40));
console.log(divide(30,40));